#include "libft.h"

void		*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	while (n--)
	{
		*d++ = *s;
		if (*s++ == c)
		{
			return ((void *)d);
		}
	}
	return (NULL);
}
